    },

    computed: {
        formattedTime() {
            const {
                hours,
                minutes,
                seconds
            } = this.countdown;
            return `${this.padZero(hours)}:${this.padZero(minutes)}:${this.padZero(seconds)}`;
        },
    },

    methods: {
        toggleAccordionItem(index) {
            this.$set(this.accordionItems, index, {
                ...this.accordionItems[index],
                collapsed: !this.accordionItems[index].collapsed,
            });
        },

        startCountdown() {
            this.countdown.timer = setInterval(() => {
                this.updateCountdown();
            }, 1000);
        },

        updateCountdown() {
            if (this.countdown.totalSeconds > 0) {
                this.countdown.totalSeconds -= 1;
                this.countdown.hours = Math.floor(this.countdown.totalSeconds / 3600);
                this.countdown.minutes = Math.floor((this.countdown.totalSeconds % 3600) / 60);
                this.countdown.seconds = this.countdown.totalSeconds % 60;
            } else {
                this.stopCountdown();
            }
        },

        stopCountdown() {
            clearInterval(this.countdown.timer);
            // Optionally, perform any actions when the countdown reaches zero
            console.log('Countdown reached zero!');
        },

        padZero(value) {
            return value < 10 ? `0${value}` : value;
        },
    },

    mounted() {
        // Start the countdown when the component is mounted
        this.startCountdown();
    },

    beforeDestroy() {
        // Clear the timer when the component is destroyed to prevent memory leaks
        clearInterval(this.countdown.timer);
    },
};
